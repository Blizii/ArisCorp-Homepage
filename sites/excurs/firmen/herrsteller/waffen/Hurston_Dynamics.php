                    <h2 class="short-hr-left">Excurs - Waffenherrsteller: Hurston Dynamics</h2>

                    <div>
                        <Span>Hurston Dynamics ist der Haupthersteller und Verteiler des United Empire of Earth für sowohl Quantumkaskaden-Laser als auch für eine breite Auswahl an Elektronengeschützen. Sie sind außerdem der größte, outgesourcete Hersteller von Standardmunitionssprengköpfen für Militärverträge und die drittgrößte Raffinerie für Antimaterie-Vorläufer in der heutigen Wirtschaft. Auch wenn wenige, fertiggestellte Produkte Hurstons Namen tragen, ihre Rohmaterialien und hergestellten Komponenten werden in nahezu jedem Stück Weltraumtechnik gefunden, welches heute durch die Gegend fliegt</Span>

                        <br>
                        <br>

                        <span>Kopien von Hurston Mikrochips sind in Produktionsstätten überall in der Galaxie zu finden und illegale Kopien sogar hinter der Grenze in Xi’An Produktionsstandorten.</span>

                        <br>
                        <br>

                        <span>Die Hurston Familie hilft den Menschen bereits seit Generationen dabei, gegeneinander Krieg zu führen. Ihr Name geht über 500 Jahre zurück, zu der Entwicklung der ersten Power-Laser für Weltall-Verteidigungsplattformen. Abstrusere Familiengeschichten verbinden die Hurstons der aktuellen Zeit mit dem Mittelalter auf der Erde und schreiben der Familie alles von Breitäxten und Armbrüsten bis hin zu Nurflügler-Fluggeräten und Atomwaffen zu</span>

                        <br>
                        <br>

                        <Span>Das aktuelle Familienoberhaupt, “Colonel” Gavin E. Stanton, nimmt seine Hintergrundgeschichte mit vollem Herzen an, dekoriert sein Büro mit Jahrtausenden von Tötungswerkzeugen und lässt, wo immer es geht, mittelalterliche Waffen in die Werbung und Leitkultur der Firma einfließen. Egal, wie die wahre Geschichte lautet, das altbekannte Sprichwort trifft auf die aktuelle Familiengeneration zu: Krieg ist gut für’s Geschäft. Hurston Dynamics ist einer der wenigen Mega-Konzerne, die mit einem eigenen Planeten prahlen können, da sie Stanton I im Rahmen eines Geld-und-Aktien-Vertrags der UEE Regierung abkauften – ein Deal mit einem geschätzten Wert von 50 Billionen Credits. Vor dem Kauf von Stanton I operierte Hurston von der Erde aus, mit Bergbauplätzen und Waffentestgeländen überall in der Galaxie. In den letzten 50 Jahren haben sich diese signifikant in Stanton verdichtet, welches es zum perfekten Ort macht, wenn man auf der Suche nach Hurston Technologie ist.</Span>

                        <br>
                        <br>

                        <span>Ein glitzernder Showroom und eine unangenehm formelle Piloten-Bar, Viola’s, täuschen über die Gesamtsituation auf dem Planeten hinweg. Anders als die anderen Bewohner des Stanton “Büro-Parks”, hat Hurston den Planeten in vollem Bewusstsein in den Abgrund getrieben: jeder Zentimeter des Planeten, der nicht Gebäuden der Firma zugeordnet ist, wurde entweder auf der Suche nach Antimaterie-Vorläufer-Material abgebaut oder in unzähligen Forschungs- und Entwicklungstests in die Luft gesprengt. Ruß und Smog verunreinigen die Atmosphäre derart, dass die Bewohner Atemgeräte tragen müssen, wenn sie Arbeiten außerhalb ihrer Wohnmodule durchführen. Die Arbeiterzufriedenheitsbewertung auf Hurston liegt bei beeindruckenden 0,5 (von 10), und wenige Bergleute halten länger als eine Einberufungstour durch. Die Firma sucht ständig nach Arbeitern mit geringsten Fähigkeiten und die Gerüchte halten sich, dass es auf dem Planeten einen Markt für menschliche Sklaven gibt.</span>

                        <br>
                        <br>

                        <span>Egal wie die Wahrheit nun aussieht, Piraten werden sehen, dass Hurston diejenigen gut bezahlt, die sie mit unfreiwilligen Arbeitskräften versorgen und dass der Planet eine exzellente Quelle für billige Munition und Metalle ist.</span>

                        <br>
                        <br>

                        <Span>Als Resultat ihrer Missachtung von Stanton Is Umwelt, ist Hurston häufig Hauptziel von Umweltschützergruppen, inklsuve der Citizen’s Clean World Alliance (CCWA) und des Wildlife Conservation Project (WCP). Der Colonel macht keinen Hehl aus seiner Abscheu vor solchen Bewegungen und gibt regelmäßig mit der Zahl eingeborener Spezies an, die seine Firma ausgelöscht hat (Osion Köpfe und codierte [Bemerkung der Redaktion, Synonym für “gestutzte”] Flachkatzenschwänze dekorieren sein Wohnzimmer) und schürt weiter den Hass von Organisationen, die vermutlich sowieso keine radioaktive Munition von ihm kaufen würden. Es ist bekannt, dass als Antwort darauf militantere Umweltschützergruppen immer häufiger Hurston Weltallkonvois als Ziel wählen. Illegale Kopfgelder auf Hurston-Raumschiffe gibt es ziemlich viele, jedoch zahlt die Firma besser für Eskortpiloten-Aufträge.</Span>

                        <br>
                        <br>

                        <h2 style="font-family: 'Nasa'">Waffensysteme</h2>
                        <Span>Auch wenn ihre Waffentechnologie im gesamten Empire und darüber hinaus zu finden ist, produziert Hurston aktuell “nur” zwei Reihen von Schiffswaffen mit ihrem Namen drauf: Quantum-Kaskaden-Laser und Elektronengeschütze. Beide Reihen sind verfügbar für mehrere Schiffsgrößen, -konfigurationen und -zwecke. Waffengrößen reichen von winzigen “Nadel” Kanonen bis hin zu massiven Kapitalbatterien, wie man sie auf UEE Kriegsschiffen findet. Hurstons umfangreiche Erfahrung erlaubt den Verkauf beider Waffentypen sowohl in Gatling-Repeater- als auch als Einzelschuss-Kanonen-Konfiguration.Varianten der Geschütze (in verschiedenen Güteklassen) sind designed für verschiedene Märkte. Billig für den Export, Standard bis Qualität für lizenzierte, zivile Nutzung und Exzellent für das UEE Militär.</Span>

                        <br>
                        <br>

                        <span>Hurstons “Spectrum” Reihe von externen Hohlraum-Quantum-Kaskaden-Lasern wird bereits seit fast einem Jahrhundert effizient von verschiedensten Operatoren genutzt. Quantum-Kaskaden-Laser feuern elektromagnetische Bolzen, welche jedes Material, das mit ihnen in Kontakt kommt, in Plasma verwandeln. Dies gibt einen einen ordentlichen “Biss”, der in den meisten Raumkampf-Begegnungen nützlich ist. Ein Hurston Dynamics Spectrum 7 QCL wird von der Kopfgeldjägergilde als Gold Standard für “Starter” Laserkanonen empfohlen, während die Spectrum 9 Gatling Variante vom UEE für ihre Zerstörer-basierten CIWS Systeme gewählt wurde, die sich zur Zeit in Weltraumtests befinden. Während viele Firmen günstigere Quantum-Kaskaden-Laser produzieren, meist auf Hurstons Technologien basierend, gilt der Name Hurston weiterhin als starkes Gütesiegel.</span>

                        <br>
                        <br>

                        <Span>Die weniger gefeierte “Magnitude” Reihe von Elektronengeschützen wird gemeinhin als spezialisierte Waffe betrachtet. Standard UEE Einheiten nutzen keine Magnitudes, obwohl diese an einigen Raumschiffvarianten für Spezialoperationen und für die Advocacy befestigt sind. Die Geschütze entfernen Elektronen von Wasserstoffatomen und beschleunigen diese, von einem magnetischen Bolzen geleitet auf hohe Geschwindigkeiten und fokussieren sie durch eine Mulit-Stage-Linse. Ein Pilot, der für die Nutzung dieses speziellen Elektronengeschützmodells ausgebildet ist, bezeichnet sie als sehr präzise und mächtige Waffe…ein “Neuling”, der ein solches Geschütz zum ersten Mal aktiviert wird es für ziemlich nutzlos halten.</Span>

                        <br>
                        <br>

                        <Span>Weitläufige Waffenlaboratorien wurden auf Stanton I eingerichtet und Gerüchte einer dritten Reihe von Geschützen halten sich. Diese Gerichte haben im letzten Jahr viel Zulauf gefunden, als die Firma ein Kopfgeld in Rekordhöhe von 10 Millionen Credits auf einen Söldner aussetzte, der ein Testartefakt der Firma gestohlen hatte. Es ist nicht bekannt, ob das Kopfgeld kassiert wurde oder ob das Artefakt gesichert werden konnte. Beobachter sehen es als sehr unüblich an, dass Hurston konventionelle und Antimaterie-Sprengköpfe ohne ihr eigenes Liefersystem produziert. Viele glauben, dass es nur noch eine Frage der Zeit sei, bis die Firma eine vollkommen neue Art Antimaterie-basierter Projektilgeschütze präsentiert. Wenn – oder ob – es jemals passiert, wird es das Kräftegleichgewicht unter den zivilen Piloten ziemlich aus der Bahn werfen…und möglicherweise den Verlauf des Krieges gegen die Vanduul beeinflussen.</Span>

