                    <h2 class="short-hr-left">Excurs - Komponentenhersteller: Willsop Systems</h2>

                    <div>

						<h2 style="font-family: 'Nasa'">Unternehmensgeschichte</h2>
						<span>WillsOp ist ein in Croshaw ansässiges Unternehmen, das im Jahr 2902 gegründet wurde. Es entwickelte eine Raumschiff-Ziel-System-Software. WillsOps kritisches Merkmal war ein einzigartiges, wenn auch von den Kunden nicht wirklich geschätztes: Sie folgten einer strengen Philosophie. Im Gegensatz zu allen anderen Sensorfirmen auf dem Markt verwendete WillsOp keinen gemeinsamen Quellcode. Das Ergebnis war eine stabile, aber auch eine geschlossene Plattform, in die nicht ohne weiteres externe Erweiterungen integriert werden konnten.
						Das erste Projekt des Unternehmens war ein millionenschweres Angebot zur Entwicklung eines Sensor-Riggings für den Carrier-Based Strike-Vertrag der UEE Navy, der schließlich zum Gladiator-Bomber führte. Wegen ihres fehlenden Bekanntheitsgrades und wegen fehlender technischer Historie scheiterte WillsOps Angebot zugunsten der DiSys D-33 MultiSuite. </span>

						<br>
						<br>

						<span>Die Firmengründer überlegten kurz, sich aufzulösen, entschieden aber schließlich, dass sie zu sehr an ihr Produkt glaubten, um sich von ihrem ersten Rückschlag aus der Bahn werfen zu lassen.
						Mit dieser Niederlage entschied sich WillsOp für die Langstrecke und konkurrierte fortan auf dem zivilen Markt als “höherwertige” Upgrade-Option eines Drittanbieters. Zwei Jahrzehnte lang blieb das Unternehmen weitgehend unbemerkt. Es unterzeichnete keine Erstverträge und wurde – von dem kleinen Teil der Öffentlichkeit, der die Hersteller von Raumschiff-Sensoren überhaupt betrachtet – als eine zuverlässige, aber im Allgemeinen unspektakuläre Option angesehen. Doch das änderte sich plötzlich im Jahre 2922.</span>

						<br>
						<br>

						<span>Am 9. August 2922 begann sich eine digitale Pest von Raumschiff zu Raumschiff auszubreiten. Das Signal kam von einer unmarkierten Sensorboje, die im geostationären Orbit innerhalb der Sendeentfernung der zweitgrößten Handelsstraße von Terra geparkt war. Innerhalb weniger Stunden hatten hunderte Schiffe den Virus, innerhalb einer Woche hatte sich das gefährlichen Daten-Paket unwissentlich auf ein Dutzend Sternsysteme ausgebreitet.</span>

						<br>
						<br>

						<span>Und dann schlug DeathGrrrrrr zu, der den gemeinsamen Code der meisten Sensorsysteme infiltrierte – und dies gleichzeitigig und überall in der Galaxie. DeathGrrrrrs Trick hat die Raumsonde nicht deaktiviert (was angesichts der inhärenten Sicherheitskopien, die nichts mit Sensorsuiten zu tun hatten, auch extrem schwierig gewesen wäre), aber er wandelte jede infizierte Sensorsuite in den Graustufenmodus um. Der Verkehr geriet ins Stocken, bis ein Fix aufgespielt werden konnte. Milliarden UEC gingen verloren.</span>

						<br>
						<br>

						<span>Der Programmierer von DeathGrrrrrr wurde nie gefasst oder identifiziert, und es bleibt unbekannt, ob der Angriff als Streich oder etwas noch Gefährlicheres gedacht war. Eine Analyse der Software und der Drohne ergab nichts. Aber die Methode der Infektion war klar: Es war ein kleines Wurm-Skript, das direkt an den Common-Core-Code geliefert wurde. Und zweifellos war der Gewinner in dieser Situation WillsOp.</span>

						<br>
						<br>

						<span>Denn mit ihrem einzigartigen Code waren die Sensoren von WillsOp von dem Angriff nicht betroffen. Über Nacht wurde das Unternehmen zu einem galaxieweiten Begriff und der Umsatz stieg in unbekannte Höhen. Einige Nachrichten gingen so weit zu sagen, dass sogar die Firma selbst hinter dem Angriff stecken könnte. Wie dem auch sei, bis zum Ende des Geschäftsjahres hatte WillsOp einen Marktanteil von 35 Prozent im Bereich der privaten Raumfahrtsensoren erobert – von zuvor unter einem Prozent. Mit dem zusätzlichen Umsatz diversifizierte sich das Unternehmen und baute neben der Weiterentwicklung seiner proprietären Software auch robuste physische Radargeräte und Scanner.</span>

						<br>
						<br>

						<span>Heute ist WillsOp Marktführer in allen Bereichen der Sensorik und wird routinemäßig bei Raumfahrzeugen der Spitzenklasse eingebaut, wobei bestimmte Spitzenmodelle mit ihrer Technologie als Standardinstallation ausgeliefert werden. Auch das Militär setzt die Sensoren ein: WllsOp-Pakete sind Standard in mehreren fortgeschrittenen UEE-Raumschiffen.</span>

						<br>
						<br>

						<h2 style="font-family: 'Nasa'">Targeting</h2>
						<span>Heute ist WillsOps Hauptverkäufer der P3-Autocompensatoren, eine Softwareinstallation, die für fast alle zivilen Raumfahrzeuge konfiguriert werden kann. Im Wesentlichen kann von der Drake Herald bis zur MISC Hull D alles mit P3-Software ausgestattet werden. Das P3 ist eines der besten Zielverfolgungssysteme auf dem Markt, das in der Lage ist, bis zu drei Raumfahrzeuge direkt und (mit angeschlossenem WillsOp-Gerät, erforderlich) bis zu 512 Schiffe in einer Standardinteraktionssphäre passiv zu verfolgen. Original Systems hat das P3-Interface-Design für den „Arena Commander“ lizenziert. Origin Jumpworks hat außerdem einen Vertrag mit WillsOp unterzeichnet, der sie zum exklusiven Lieferanten von maßgeschneiderten Zielsystemen für die Origin 325a macht. Das von WillsOp entwickelte System wird mit dem Modell 2944 des 325a Premiere haben und wird von Raumfahrt-Enthusiasten mit Spannung erwartet.</span>

						<br>
						<br>

						<h2 style="font-family: 'Nasa'">Verfolgen</h2>
						<span>WillsOp ist nicht nur für Direkt-Targeting-Systeme zuständig, sondern auch für passive Tracking-Systeme, die sich mitlerweile zu einem branchenführenden Standard entwickelt haben. Wo sich eine Targeting-Suite typischerweise darauf konzentriert, so viele Informationen wie möglich über ein Ziel zu extrahieren, sind Tracking-Systeme notwendig, um alles andere im Auge zu behalten. </span>

						<br>
						<br>

						<span>Die Tracker von WillsOp sind in der Lage, Dutzende von Identifikatoren für die sofortige Zielerfassung zur Verfügung zu stellen. Während jede WillsOp Targeting Suite eine proprietäre WillsOp Tracking Software benötigt, ist der Tracker eigentlich herstellerneutral. WillsOp Tracker werden in OEM-Modellen hergestellt und den Herstellern von Raumfahrzeugen zum Selbstkostenpreis zur Verfügung gestellt. Das Ziel des Unternehmens ist einfach: die Nutzer an ihr Ökosystem heranzuführen und sie dann zu binden, wenn sie sich für ein Targeting-System entscheiden.</span>

						<br>
						<br>

						<h2 style="font-family: 'Nasa'">Tiefenraum-Radar</h2>
						<span>Es war ein Moment des erlösenden Ruhmes für WillsOp, als sie nach fast vier Jahrzehnten endlich einen wertvollen, langfristigen UEE-Militärvertrag erhielten. WillsOp hat sich mit Anvil Aerospace und der UEE Navy zusammengetan, um die die Software für das Long-Look Radar-System zu programmieren, die in jeder F7A-R Hornet-Tracker installiert ist. Das kuppelförmige Gehäuse wird in den oberen Turmschlitz der Hornet eingebaut und ersetzt die beweglichen Geschütze durch ein leistungsstarkes Radar.</span>

						<br>
						<br>

						<span>Der LongLook bietet die bestmögliche Sicht auf das Schlachtfeld in einem einsitzigen Jäger, mit der Rechenleistung zur Verfolgung von Flottenbewegungen und zur Koordination von Einsätzen auf Geschwaderebene. Ein ziviles Modell des LongLook ist ebenfalls erhältlich und kann in jedes Hornet-Modell anstelle des Standard-Frachtcontainers eingebaut werden.
						Die Zukunft des Unternehmens sieht rosig aus, obwohl die Sicherheitsmechanismen seit dem DeathGrrrrrr-Angriff andere Softwaresysteme ebenfalls wieder florieren lassen. Beobachter bezeichnen WillsOps Mangel an Spezial-Sensoroptionen, zum Beispiel metalldurchdringende Minenscanner, häufig als eine Quelle von Umsatzeinbußen. WillsOp seinerseits scheint mit seiner Standard-Sensorik zufrieden zu sein und überlässt Spezial-Senorern lieber den Mitwettbewerbern.</span>

